package uk.ac.ebi.fg.annotare2.submission.model;

/**
 * @author Olga Melnichuk
 */
public enum FileType implements EnumWithHelpText{

    RAW_FILE("Raw", "The \"native\" files generated by the microarray scanner software (e.g. .CEL, .gpr) or sequencing machine (.fastq.gz)"),

    RAW_MATRIX_FILE("Raw Matrix", "A raw file in tab-delimited text (.txt) format, that contains data from more than one assay (probes/genes in rows, assays in columns)"),

    PROCESSED_FILE("Processed", "A tab-delimited text (.txt) file containing data from a single assay"),

    PROCESSED_MATRIX_FILE("Processed Matrix", "A tab-delimited text (.txt) file of very strict format, containing data from more than one assay");

    private final String title;
    private final String helpText;

    FileType(String title, String helpText) {
        this.title = title;
        this.helpText = helpText;
    }

    public String getHelpText() {
        return helpText;
    }


    public String getTitle() {
        return title;
    }

    @Override
    public String toString() {
        return title;
    }

    @SuppressWarnings("unused")
    public boolean isRaw() {
        return RAW_FILE == this || RAW_MATRIX_FILE == this;
    }

    @SuppressWarnings("unused")
    public boolean isProcessed() {
        return PROCESSED_FILE == this || PROCESSED_MATRIX_FILE == this;
    }

    @SuppressWarnings("unused")
    public boolean isFGEM() {
        return RAW_MATRIX_FILE == this || PROCESSED_MATRIX_FILE == this;
    }
}
